#' GetGaData
#' Execute a ganalytics query.
#' @param query the query to execute.
#' @param quiet supress messages
#' @param details show detailed messages
#' @param .progress progress bar to display. use .progress = "none" to turn off.
#' @return a dataframe
#' @export
GetGaData <- function(
  query,
  quiet = FALSE,
  details = FALSE,
  .progress = "time",
  use_oob = FALSE,
  addViewId = FALSE
) {
  appname <- "GANALYTICS"
  scope <- "https://www.googleapis.com/auth/analytics.readonly"
  cache <- query@authFile
  userName <- query@userName
  credsFile <- query@appCreds
  creds <- list(
    app = app_oauth_creds(
      appname = appname,
      creds = credsFile
    ),
    user = list(
      login = userName,
      cache = cache
    ),
    use_oob = use_oob
  )
  queryParams <- GetGaQueries(query)
  responses <- alply(
    .data = queryParams,
    .margins = 2,
    .fun = GaPaginate,
    maxRequestedRows = GaMaxResults(query),
    creds = creds,
    quiet = quiet,
    details = details,
    .progress = .progress
  )
  data <- ldply(
    .data = responses,
    .fun = function(response) {
      df <- response$data
      if(addViewId & nrow(df) >= 1) {
        df <- mutate(df, viewId = response$viewId)
      }
      attr(df, "sampleSize") <- response$sampleSize
      attr(df, "sampleSpace") <- response$sampleSpace
      return(df)
    }
  )[-1]
  sampled <- any(laply(responses, function(response) {isTRUE(response$sampled)}))
  if (sampled) {
    warning("Contains sampled data.")
  }
  return(data)
}
